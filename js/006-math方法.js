//Math.max/min():返回最大/小值
console.log(Math.max(...[1,2,3,4,5,6,],10,22))
console.log(Math.min(...[1,2,3,4,5,6,],10,22))

//Math.random()，返回的是一个[0.1)的随机数
console.log(Math.random())
//返回[1-11)的随机数
console.log(Math.random()*10+1)
//返回[2-12)的随机数
console.log(Math.random()*10+2)

//Math.ceil():向上取整
console.log(Math.ceil(3.2))   //4
console.log(Math.ceil(3.9))   //4

//Math.floor():向下取整
console.log(Math.floor(3.2))   //3
console.log(Math.floor(3.9))   //3

//Math.round():四舍五入
console.log(Math.round(3.2))   //3
console.log(Math.round(3.9))   //4
