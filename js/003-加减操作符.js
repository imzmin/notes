//1、加法操作符+

console.log(5+"5")         //55
console.log("5"+"5")       //55
console.log(5+5)           //10
console.log(5+true)        //6
console.log(4+null)        //4
console.log(4+undefined)   //NaN
console.log(4+NaN)         //NaN
console.log("5"+null)      //5null
console.log("5"+undefined) //5NaN
console.log("5"+NaN)       //5NaN

//1、减法操作符—

console.log(5-"2")         //3
console.log("5"-1)         //4
console.log("5"-"3")       //2
console.log(5-4)           //1
console.log(5-true)        //4
console.log(4-null)        //4
console.log(4-undefined)   //NaN
console.log(4-NaN)         //NaN
console.log("5"-null)      //5
console.log("5"-undefined) //NaN
console.log("5"-NaN)       //NaN
